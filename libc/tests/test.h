/* This header defines helpful symbols and macros for making a test harness. Generally, a test should
 * play well with the POSIX shell: returning 0 means success, and returning anything else means
 * failure. */

#ifndef _TEST_H
#define _TEST_H

#include <errno.h>
#include <stdlib.h>

#define ASSERT(x) do { if (!(x)) exit(EXIT_FAILURE); } while (0)

#endif /* _TEST_H */

